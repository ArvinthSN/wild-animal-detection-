# 6. Load the chosen sample image using PIL.Image.open() and convert it to RGB
original_image_pil = Image.open(sample_image_path).convert('RGB')

# 7. Perform object detection on the sample image using the loaded YOLOv5 model (`model_yolo`)
print(f"Performing YOLOv5 detection on: {sample_image_path}")
yolo_results = model_yolo(sample_image_path)

# Accessing raw detection results
# The structure of yolo_results can vary slightly depending on the YOLOv5 version and source
# For torch.hub.load('ultralytics/yolov5', ...) the results object usually has .pred attribute
# results.pred is a list of tensors, one for each image in the batch
# Each tensor has detections in format (x1, y1, x2, y2, confidence, class)

detected_objects_info = []

# 8. Iterate through each detection result from YOLOv5
# If using results.pred attribute, iterate through the first (and only) image's detections
if yolo_results.pred is not None and len(yolo_results.pred) > 0:
    detections = yolo_results.pred[0] # Get detections for the first image

    for *xyxy, conf, cls in detections:
        # a. Extract the bounding box coordinates and YOLOv5 detection confidence
        xmin, ymin, xmax, ymax = xyxy.cpu().numpy()
        yolo_confidence = conf.item()

        # Ensure coordinates are within image bounds before cropping
        xmin = max(0, xmin)
        ymin = max(0, ymin)
        xmax = min(original_image_pil.width, xmax)
        ymax = min(original_image_pil.height, ymax)

        # b. Crop the detected object from the original PIL image using the crop_object_from_image function
        cropped_object_pil = crop_object_from_image(original_image_pil, (xmin, ymin, xmax, ymax))

        # c. Preprocess the cropped image using the preprocess_image_for_classifier function
        #    and move it to the appropriate device
        preprocessed_cropped_image = preprocess_image_for_classifier(cropped_object_pil)

        # d. Pass the preprocessed cropped image through the classification model to get predictions
        with torch.no_grad():
            classifier_output = model_classifier(preprocessed_cropped_image)
            probabilities = torch.nn.functional.softmax(classifier_output, dim=1)

            # e. Determine the predicted class by finding the index with the highest probability
            predicted_class_idx = torch.argmax(probabilities, dim=1).item()
            predicted_class_confidence = probabilities[0, predicted_class_idx].item()

            # f. Map the predicted class index to its corresponding class name
            predicted_class_name = class_names[predicted_class_idx]

            # g. Store the original bounding box, YOLOv5 detection confidence, and the classification model's
            #    predicted class name and its confidence.
            detected_objects_info.append({
                'bbox': [xmin, ymin, xmax, ymax],
                'yolo_conf': yolo_confidence,
                'classifier_class': predicted_class_name,
                'classifier_conf': predicted_class_confidence
            })
else:
    print("No objects detected by YOLOv5 or results not in expected format.")

# 9. Prepare a way to visualize the combined results.
# Create a copy of the original image to draw on
draw_image = original_image_pil.copy()
